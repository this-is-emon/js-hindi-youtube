// does Ts is statically typed or dynamically typed ?
/*
TypeScript is statically typed. 

Static typing means that variable types are known and checked at compile-time rather than runtime. 
In TypeScript, you declare the type of a variable explicitly, and the TypeScript compiler enforces 
that the types are used correctly. This helps catch type-related errors early in the development 
process, providing better code quality and reducing runtime errors.

Contrastingly, dynamically typed languages, like JavaScript, determine types at runtime. 
Variables in dynamically typed languages can hold values of any type, and type checking occurs 
during program execution.

By introducing static typing, TypeScript enhances the development experience by offering 
better tooling support, code completion, and catching potential errors before running the code.
*/
